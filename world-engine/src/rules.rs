// R001: Token Consumption
// ═══════════════════════════════════════════════════════════════════════════

/// R001: Token consumption rule.
///
/// Each tick, agents lose tokens based on their phase and skills.
/// Uses `TokenBurnEngine` for the actual calculation.
pub struct TokenConsumptionRule {
    engine: TokenBurnEngine,
}

impl TokenConsumptionRule {
    /// Create with default consumption config.
    pub fn new() -> Self {
        Self {
            engine: TokenBurnEngine::with_defaults(),
        }
    }

    /// Create with custom consumption config.
    pub fn with_config(config: ConsumptionConfig) -> Self {
        Self {
            engine: TokenBurnEngine::new(config),
        }
    }

    /// Get a reference to the underlying engine.
    pub fn engine(&self) -> &TokenBurnEngine {
        &self.engine
    }
}

impl Default for TokenConsumptionRule {
    fn default() -> Self {
        Self::new()
    }
}

impl Rule for TokenConsumptionRule {
    fn id(&self) -> &str {
        "R001"
    }

    fn name(&self) -> &str {
        "Token Consumption"
    }

    fn priority(&self) -> u32 {
        100
    }

    fn evaluate(&self, _ctx: &RuleContext, agent: &mut AgentRecord) -> RuleResult {
        // Skip dead or birth-phase agents
        if agent.phase == AgentPhase::Dead || agent.phase == AgentPhase::Birth {
            return RuleResult::empty(self.id());
        }

        let burn_amount = self.engine.calculate_tick_burn(agent);
        if burn_amount == 0 {
            return RuleResult::empty(self.id());
        }

        let tokens_before = agent.tokens;
        let actual_burn = burn_amount.min(agent.tokens);
        agent.tokens -= actual_burn;

        let mut events = Vec::new();

        if actual_burn > 0 {
            events.push(WorldEvent::BalanceChanged {
                agent_id: agent.id.to_string(),
                currency: crate::world::enums::Currency::Token,
                old_balance: tokens_before,
                new_balance: agent.tokens,
            });
        }

        RuleResult::with_events(self.id(), events)
    }
}

