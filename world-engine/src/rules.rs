// R002: Death Judgment
// ═══════════════════════════════════════════════════════════════════════════

/// R002: Death judgment rule.
///
/// Checks if an agent's tokens have reached zero. When tokens are depleted,
/// the agent transitions through dying -> dead phases.
pub struct DeathJudgmentRule {
    /// Number of grace ticks after tokens hit zero before actual death.
    pub grace_ticks: u64,
}

impl DeathJudgmentRule {
    /// Create with default grace period (0 ticks = immediate death).
    pub fn new() -> Self {
        Self { grace_ticks: 0 }
    }

    /// Create with a custom grace period.
    pub fn with_grace_ticks(grace_ticks: u64) -> Self {
        Self { grace_ticks }
    }
}

impl Default for DeathJudgmentRule {
    fn default() -> Self {
        Self::new()
    }
}

impl Rule for DeathJudgmentRule {
    fn id(&self) -> &str {
        "R002"
    }

    fn name(&self) -> &str {
        "Death Judgment"
    }

    fn priority(&self) -> u32 {
        200
    }

    fn evaluate(&self, _ctx: &RuleContext, agent: &mut AgentRecord) -> RuleResult {
        // Skip agents already dead
        if agent.phase == AgentPhase::Dead {
            return RuleResult::empty(self.id());
        }

        // Skip birth-phase agents (protected)
        if agent.phase == AgentPhase::Birth {
            return RuleResult::empty(self.id());
        }

        // Check if tokens are depleted
        if agent.tokens > 0 {
            return RuleResult::empty(self.id());
        }

        let mut events = Vec::new();

        // Agent is dying
        events.push(WorldEvent::AgentDying {
            agent_id: agent.id.to_string(),
            reason: DeathReason::TokenDepleted,
            grace_ticks: self.grace_ticks,
        });

        // If grace period is 0, immediately transition to dead
        if self.grace_ticks == 0 {
            agent.phase = AgentPhase::Dead;
            events.push(WorldEvent::AgentDied {
                agent_id: agent.id.to_string(),
                reason: DeathReason::TokenDepleted,
            });
        }
        // If grace > 0, the agent enters Dying phase and will be
        // fully killed after grace_ticks have elapsed (handled externally
        // or by a future grace-tracking mechanism).

        RuleResult::with_events(self.id(), events)
    }
}

